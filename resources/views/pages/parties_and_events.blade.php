        $.ajaxSetup({
            headers: {
                'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
            }
        });

        $(".btn-submit").click(function(e){

            e.preventDefault();

            var fname = $("#fname").val();
            var lname = $("#lname").val();
            var email = $("#email").val();
            var phone = $("#mobile").val();
            var message = $("#message").val();
            var name = fname+lname;

            $.ajax({
                type:'POST',
                url:"{{ route('contact_us') }}",
                data:{name:name, email:email, phone:phone, message:message},
                success:function(data){
                    if($.isEmptyObject(data.error)){
                        $('.contact-from').css('display','none');
                        $('.con-thnx').css('display','block');
                    }else{
                        console.log(data);

                        if (data.error.message){
                            $('#error-msg').css('color','red').html(data.error.message[0]);
                        }else{
                            $('#error-msg').html('');
                        }
                        if (data.error.phone){
                            $('#error-phone').css('color','red').html(data.error.phone[0]);
                        }else{
                            $('#error-phone').html('');
                        }

                        if (data.error.email){
                            $('#error-email').css('color','red').html(data.error.email[0]);
                        }else{
                            $('#error-email').html('');
                        }

                        if (data.error.name){
                            $('#error-name').css('color','red').html(data.error.name[0]);
                        }else{
                            $('#error-name').html('');
                        }

                    }
                }
            });

        });


        $(".btbt").click(function() {
            $('html, body').animate({
